package io.github.andronikus.animation4jsample;

public class RobotState {

    boolean robotHappy;
    boolean robotSad;

    public boolean isRobotHappy() {
        return robotHappy;
    }

    public boolean isRobotSad() {
        return robotSad;
    }

    public void setRobotHappy(boolean happy) {
        robotHappy = happy;
    }

    public void setRobotSad(boolean sad) {
        robotSad = sad;
    }
}
